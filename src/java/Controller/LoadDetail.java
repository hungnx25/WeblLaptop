    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        HttpSession ses = request.getSession();
        String id = request.getParameter("id");
        Products p = pdao.getProductById(id);
        ProductDetails pd = pdao.getProductDetailById(id);
        ArrayList<Products> pro = pdao.getProductDesc();

        ses.setAttribute("pro", pro);
        ses.setAttribute("p", p);
        ses.setAttribute("pd", pd);
       
        PageInfor page1 = (PageInfor) ses.getAttribute("page");
        if (page1 == null) {
            page1 = new PageInfor(1, 4, pro.size());
            page1.calc();
            ses.setAttribute("page1", page1);
        }

        response.sendRedirect(request.getContextPath() + "/View/Product.jsp");
    }

